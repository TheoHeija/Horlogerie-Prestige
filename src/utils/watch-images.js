/**
 * Find the best matching image for a watch based on brand and name
 * 
 * @param {Object} product - The product data with brand and name
 * @param {string} product.brand - The brand of the watch
 * @param {string} product.name - The name/model of the watch
 * @returns {string} - URL to the best matching image or placeholder
 */
export const findWatchImage = (product) => {
    // If no product or critical fields missing, return placeholder
    if (!product || !product.brand) {
        return '/images/placeholder-watch.svg';
    }

    // Default to the product.image_url if it exists
    if (product.image_url) {
        return product.image_url;
    }

    // Try to find a high-quality image based on brand and model
    const brandKey = product.brand.toLowerCase().replace(/\s+/g, '-');
    const name = product.name ? product.name.toLowerCase() : '';

    // Check if we have images for this brand
    if (watchImages[brandKey]) {
        // First try to find a model that matches by name
        for (const model of watchImages[brandKey]) {
            if (name.includes(model.name.toLowerCase()) ||
                model.name.toLowerCase().includes(name)) {
                return model.images[0];
            }
        }

        // If no specific match, return the first image from the collection
        return watchImages[brandKey][0]?.images[0] || '/images/placeholder-watch.svg';
    }

    // Check if this is a multi-word brand that might match partially
    const brandParts = product.brand.toLowerCase().split(/\s+/);
    for (const brandPart of brandParts) {
        for (const key in watchImages) {
            if (key.includes(brandPart)) {
                return watchImages[key][0]?.images[0] || '/images/placeholder-watch.svg';
            }
        }
    }

    // Fallback to placeholder
    return '/images/placeholder-watch.svg';
};

export default watchImages; 